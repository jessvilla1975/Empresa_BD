    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        System.out.println("Se presionó el botón guardar");
        int nump = Integer.parseInt(nit.getText());
        int codm = Integer.parseInt(codigo.getText());
        int cant = Integer.parseInt(codigo.getText());
        String item = (String) tipo.getSelectedItem();
        String item2 = (String) uni.getSelectedItem();
      
        crud.insertarMateria(codm, item, des.getText(),cant, item2);
        crud.insertarProveedor(nump, nombre.getText(), nomcontacto.getText(), 
            telefono.getText(), direccion.getText());
        
        
        nit.setText("Ingresar nit del proveedor");
        nombre.setText("Ingresar nombre del proveedor");
        nomcontacto.setText("Ingresar nombre de contacto");
        telefono.setText("Ingresar telefono");
        direccion.setText("Ingresar direccion");
        codigo.setText("Ingresar codigo de materia prima");
        des.setText("Ingresar descripcion de materia prima");
        cantidad.setText("Ingresar cantidad de existencia de materia prima");
   
        setVisible(false);
    }//GEN-LAST:event_GuardarActionPerformed

